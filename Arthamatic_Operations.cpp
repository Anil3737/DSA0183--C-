# include <iostream>
using namespace std;
int main()
{
	float num1,num2;
	float add,sub,mul,div;
	cout<<"Enter First Number:";
	cin>>num1;
	cout<<"Enter Second Number:";
	cin>>num2;
	add = num1 + num2;
	sub = num1 - num2;
	mul = num1 * num2;
	div = num1 / num2;
	cout<<"Addition::"<<add<<endl;
	cout<<"Subtraction::"<<sub<<endl;
	cout<<"Multiplication::"<<mul<<endl;
	cout<<"Division::"<<div<<endl;
}
